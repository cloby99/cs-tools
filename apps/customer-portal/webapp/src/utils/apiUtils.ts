/**
 * Builds standard API request headers for authenticated requests.
 *
 * @param {string} idToken - The ID token for authentication.
 * @returns {Record<string, string>} Headers object for fetch requests.
 */
export function addApiHeaders(idToken: string): Record<string, string> {
  return {
    "Content-Type": "application/json",
    Accept: "application/json",
    Authorization: `Bearer ${idToken}`,
    "x-user-id-token": idToken,
  };
}
